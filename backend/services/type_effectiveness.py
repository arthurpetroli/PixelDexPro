def calculate_type_effectiveness(defending_types: list[str]) -> dict[str, float]:
    effectiveness = {}
    for attacking_type in ALL_TYPES:
        multiplier = 1.0
        for defending_type in defending_types:
            defending_type = defending_type.lower()
            if (
                defending_type in TYPE_CHART
                and attacking_type in TYPE_CHART[defending_type]
            ):
                multiplier *= TYPE_CHART[defending_type][attacking_type]
            else:
                multiplier *= 1.0
        effectiveness[attacking_type] = round(multiplier, 4)
    return effectiveness


def get_weaknesses(effectiveness: dict[str, float]) -> dict[str, float]:
    return {t: m for t, m in effectiveness.items() if m > 1.0}


def get_resistances(effectiveness: dict[str, float]) -> dict[str, float]:
    return {t: m for t, m in effectiveness.items() if 0.0 < m < 1.0}


def get_immunities(effectiveness: dict[str, float]) -> list[str]:
    return [t for t, m in effectiveness.items() if m == 0.0]


def get_quadruple_weaknesses(effectiveness: dict[str, float]) -> list[str]:
    return [t for t, m in effectiveness.items() if m >= 4.0]


def get_quadruple_resistances(effectiveness: dict[str, float]) -> list[str]:
    return [t for t, m in effectiveness.items() if m <= 0.25 and m > 0.0]


def get_strategic_tags(
    pokemon_types: list[str], effectiveness: dict[str, float]
) -> list[str]:
    tags = []
    weaknesses = get_weaknesses(effectiveness)
    immunities = get_immunities(effectiveness)
    quad_weak = get_quadruple_weaknesses(effectiveness)

    if len(immunities) > 0:
        tags.append("Has Immunities")
    if len(quad_weak) > 0:
        tags.append("4x Weakness")
    if len(weaknesses) <= 2:
        tags.append("Few Weaknesses")
    if len(weaknesses) >= 5:
        tags.append("Many Weaknesses")

    for t in pokemon_types:
        if t.lower() == "steel":
            tags.append("Steel Resilience")
        if t.lower() == "fairy":
            tags.append("Dragon Immune")
        if t.lower() == "ghost":
            tags.append("Normal Immune")
        if t.lower() == "flying":
            tags.append("Ground Immune")

    return list(set(tags))


def calculate_team_coverage(team_types: list[list[str]]) -> dict:
    all_weaknesses = {}
    all_resistances = {}
    all_immunities = []

    for pokemon_types in team_types:
        eff = calculate_type_effectiveness(pokemon_types)
        weak = get_weaknesses(eff)
        res = get_resistances(eff)
        imm = get_immunities(eff)

        for t, m in weak.items():
            if t not in all_weaknesses:
                all_weaknesses[t] = []
            all_weaknesses[t].append(m)

        for t, m in res.items():
            if t not in all_resistances:
                all_resistances[t] = []
            all_resistances[t].append(m)

        all_immunities.extend(imm)

    team_weaknesses = {}
    for t, multipliers in all_weaknesses.items():
        team_weaknesses[t] = round(sum(multipliers) / len(multipliers), 4)

    shared_weaknesses = {}
    for t, multipliers in all_weaknesses.items():
        if len(multipliers) >= 2:
            shared_weaknesses[t] = len(multipliers)

    problematic = sorted(team_weaknesses.items(), key=lambda x: x[1], reverse=True)[:5]

    return {
        "weaknesses": team_weaknesses,
        "resistances": {
            t: round(sum(m) / len(m), 4) for t, m in all_resistances.items()
        },
        "immunities": list(set(all_immunities)),
        "shared_weaknesses": shared_weaknesses,
        "problematic_types": [t for t, _ in problematic if team_weaknesses[t] > 1.0],
    }


class TypeEffectiveness:
    """Helper class for type effectiveness calculations."""

    def calculate(self, defending_types: list[str]) -> dict[str, float]:
        return calculate_type_effectiveness(defending_types)

    def get_weaknesses(self, effectiveness: dict[str, float]) -> dict[str, float]:
        return get_weaknesses(effectiveness)

    def get_resistances(self, effectiveness: dict[str, float]) -> dict[str, float]:
        return get_resistances(effectiveness)

    def get_immunities(self, effectiveness: dict[str, float]) -> list[str]:
        return get_immunities(effectiveness)

    def get_defensive_profile(
        self, pokemon_types: list[str]
    ) -> tuple[dict[str, float], dict[str, float], set[str]]:
        """
        Calculate the complete defensive profile for a Pokemon.

        Returns:
            Tuple of (weaknesses, resistances, immunities)
        """
        effectiveness = self.calculate(pokemon_types)
        weaknesses = self.get_weaknesses(effectiveness)
        resistances = self.get_resistances(effectiveness)
        immunities = set(self.get_immunities(effectiveness))
        return weaknesses, resistances, immunities
